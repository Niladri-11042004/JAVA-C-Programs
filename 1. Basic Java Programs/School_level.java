class School_level
{
    public static void main(int c)
    {
        if (c>=1&&c<=4)
        {
            System.out.println("you study in junior school ");
        }
        else if(c>=5&&c<=8)
        {
            System.out.println("you study in middle school");
        }
        else if(c>=9&&c<=12)
        {
            System.out.println("you study in senior school");
        }
        else 
        {
            System.out.println("you study in college");
        }
    }
}